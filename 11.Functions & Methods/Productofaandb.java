public class Productofaandb{
    public static int multiply(int a, int b){
        int mul=a*b;
        return mul;
    }
    public static void main(String args[]){
        int a=15;
        int b=2;
        int prod=multiply(a, b);
        System.out.println("The Product of a and b is: " + prod);

    }
}