public class Switchcase{
    public static void main(String args[]){
        int num=2;
        switch (num){
            case 1: System.out.println("i want cycle");
            break;
            case 2: System.out.println("i want car");
            break;
            case 3: System.out.println("i want plane");
            break;
            default : System.out.println("i want nothing");
        }
    }
}