public class LogicalOperators{
    public static void main(String args[]){
        System.out.println((12>10)&&(10<12));
        System.out.println((12<10)&&(10<12));
        System.out.println((12>10)||(10<12));
        System.out.println((12<10)||(10<12));
        System.out.println(!(12>10));
        System.out.println(!(12<10));

    }
}